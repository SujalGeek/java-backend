class literal {
  public static void main(String args[]) {
    int num1 = 0b101;
    int num2 = 0x7E;
    int num3 = 10_00__000;
    double num4 = 1.234_567_89;
    double num5 = 56;
    System.out.println("Num1: " + num1 + ", Num2: " + num2 + ", Num3: " + num3 + ", Num4: " + num4 + ", Num5: " + num5);
  }
}