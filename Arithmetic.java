public class Arithmetic {
  public static void main(String[] args) {
    int number1 = 10; // Intializing number1
    int number2 = 20; // Intializing number2
    int sum_of_numbers = number1 + number2; // Adding two numbers
    System.out.println("Sum of " + number1 + ", " + number2 + "is: " + sum_of_numbers);
    int difference_of_numbers = number2 - number1; // Subtracting two numbers
    System.out.println("Difference of " + number2 + ", " + number1 + "is: " + difference_of_numbers);
    int product_of_numbers = number1 * number2; // Multiplying two umbers
    System.out.println("Product of " + number1 + ", " + number2 + "is: " + product_of_numbers);
    int quotient_of_numbers = number2 / number1; // Dividing two numbers
    System.out.println("Quotient of " + number2 + ", " + number1 + "is: " + quotient_of_numbers);

  }

}
